#########################################################################################################################
# Algorithmus um Fantansy Football Points zu predicten
# Nutzung des ffanalytics Packages
# https://fantasyfootballanalytics.net/2016/06/ffanalytics-r-package-fantasy-football-data-analysis.html
#########################################################################################################################
# Environment löschen
rm(list = ls())

# Packages laden
library(ffanalytics)

# Daten laden
# Projections von verschiedenen Quellen scrapen
HData <- scrape_data(src = c("CBS", "ESPN", "FantasyData", "FantasyPros", "FantasySharks", "FFToday",
                              "FleaFlicker", "NumberFire", "Yahoo", "FantasyFootballNerd", "NFL", "RTSports",
                              "Walterfootball"),
                     pos = c("QB", "RB", "WR", "TE", "K"),
                     season = 2020,
                     week = 0)

# Predictions für die 2021 Saison erstellen mit sleeper Scoring Rules-----------------------------------------------------
Preds <- projections_table(HData, scoring_rules = list(
  pass = list(
    pass_att = 0, pass_comp = 0, pass_inc = 0, pass_yds = 0.04, pass_tds = 4,
    pass_int = -1, pass_40_yds = 0,  pass_300_yds = 0, pass_350_yds = 0,
    pass_400_yds = 0
  ),
  rush = list(
    all_pos = TRUE,
    rush_yds = 0.1,  rush_att = 0, rush_40_yds = 0, rush_tds = 6,
    rush_100_yds = 0, rush_150_yds = 0, rush_200_yds = 0),
  rec = list(
    all_pos = TRUE,
    rec = 0, rec_yds = 0.1, rec_tds = 6, rec_40_yds = 0, rec_100_yds = 0,
    rec_150_yds = 0, rec_200_yds = 0
  ),
  misc = list(
    all_pos = TRUE,
    fumbles_lost = -2, fumbles_total = -1,
    sacks = 0, two_pts = 2
  ),
  kick = list(
    xp = 1.0, fg_0019 = 3.0,  fg_2029 = 3.0, fg_3039 = 3.0, fg_4049 = 4.0,
    fg_50 = 5.0,  fg_miss = -1.0
  ),
  ret = list(
    all_pos = TRUE,
    return_tds = 6, return_yds = 0
  ),
  idp = list(
    all_pos = TRUE,
    idp_solo = 1, idp_asst = 0.5, idp_sack = 2, idp_int = 3,  idp_fum_force = 3,
    idp_fum_rec = 2,  idp_pd = 1, idp_td = 6,  idp_safety = 2
  ),
  dst = list(
    dst_fum_rec = 2,  dst_int = 2, dst_safety = 2, dst_sacks = 1, dst_td = 6,
    dst_blk = 2, dst_ret_yds = 0, dst_pts_allowed = 0
  ),
  pts_bracket = list(
    list(threshold = 0, points = 10),
    list(threshold = 6, points = 7),
    list(threshold = 20, points = 4),
    list(threshold = 34, points = 0),
    list(threshold = 99, points = -4)
  )
))

# Player info hinzufügen-------------------------------------------------------------------------------------------------
Preds <- Preds %>%
  add_player_info() %>%
  arrange(rank)

# Kleine Bereinigungen
Preds <- Preds %>%
  mutate_if(is.numeric, round, digits = 2) %>%
  select(-V1)

# avg_type == average
Preds <- Preds %>%
  filter(avg_type == "average")


# Daten speichern für Nutzung in shiny App
save(Preds, file = "C:/Users/janni/OneDrive - EUFH GmbH/R-Uebungen/Football Stuff/Fantasy Football/Shiny_App/Predictions.RData")

# Top Draft Rank nach Position
Preds %>%
  group_by(position) %>%
  summarise(min(rank))

# WR analysieren
Preds %>% 
  filter(pos == "WR") %>%
  add_player_info() %>%
  arrange(pos_rank) %>%
  view()


